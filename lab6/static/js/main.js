function remove_feed(feed_id) {
	let id = 'feed-' + feed_id;
	let current_id = $('#header > div')[1].id;
	feed_menu= document.getElementById(`feed-${feed_id}`);
	$.ajax({
            url: "/rest/feed/remove",
            type: "DELETE",
            data: {"url": feed_id},
            contentType: 'application/json; charset=utf-8',

            success: function (resultData) {
                if (resultData['error']) {
                    // TODO inform user if doesn't exist
                    return;
                }
                $('#menu > ul')[0].removeChild(feed_menu);
                if (feed_id == current_id){
                	document.location.href= '../../';
				}
            },
            error: function (jqXHR, textStatus, errorThrown) {
            },
            timeout: 60000,
        });
}